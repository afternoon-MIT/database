class Vehicle:
    def __init__(self, make, color, owner, wheels, engine, ):
        self.make = make
        self.color = color
        self.owner = owner
        self.wheels = wheels
        self.engine = engine


class House:
    def __init__(self, make, color, owner, windows, doors):
        self.owner = owner
        self.color = color
        self.make = make
        self.windows = windows
        self.doors = doors
